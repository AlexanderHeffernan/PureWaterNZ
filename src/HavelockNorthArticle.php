          <h1>Havelock North’s Drinking Water Outbreak</h1>
          <p class="article-meta"><strong>Author:</strong>  Benj Baniqued, <strong>Published:</strong> 27/05/2020</p>     

          <div class="image-wrapper">
            <img data-enlargable src="images/havelockNorthThumbnail.png">
            <span>Image Source: https://www.pxfuel.com/en/free-photo-xwglx</span>
          </div> 

          <audio controls>
            <source src="audio/HavelockAudio.mp3" type="audio/mpeg">
          </audio>

          <p>
          More than 5000 people were affected by the outbreak in this city. 45 were hospitalized, possibly linked to 3 deaths and with people who still continue to suffer from its effects today. A <a href="https://www.google.com/search?q=gastrointestinal+illness&rlz=1C1CHBF_enNZ882NZ883&oq=gastrointestinal+illness&aqs=chrome..69i57j0l7.898j0j7&sourceid=chrome&ie=UTF-8">gastrointestinal</a> outbreak hit the people of Havelock North during spring in August 2016. This was described as New Zealand’s largest waterborne outbreak. 
          </p>
          <p>
          The cause was said to have been from heavy rain which then flowed contaminated water from sheep faeces to flow in the Mangateretere pond then entering the aquifer which then drew contaminated water into the water supply. 
          </p>
          <p>
          Measures have been taken to prevent outbreaks like these to happen again. This would include:
          <ul>
            <li>Require water supplies to be treated, including with a residual disinfectant in the entire reticulation.</li>
          </ul>
          <div class="image-wrapper">
            <img data-enlargable src="images/havelockNorthImage2.png">
            <span>Image Source: https://www.google.com/maps/@-41.2<br>265694,174.8818927,3a,46.4y,261.95h<br>,95.38t/data=!3m6!1e1!3m4!1sMCXMpYo<br>WdwO5Wynu6zUiyg!2e0!7i13312!8i6656?hl=en</span>
          </div> 
          <ul>
            <li>Make compliance with the Drinking-water standards mandatory.</li>
            <li>Establish a dedicated drinking water regulator.</li>
            <li>Establish aggregated, dedicated water suppliers.</li>
          </ul>
          Link to more information <a href="https://www.health.govt.nz/our-work/environmental-health/drinking-water/government-inquiry-havelock-north-drinking-water-outbreak">here</a>.
          </p>
          <p>
          Thankfully the government has made measures to prevent these problems from happening but it doesn’t mean that it won’t happen again. Finding another source of water to collect from could be more safe although it might require more effort. These other sources could be from Petone’s Te Puna Wai Ora artesian water fountain. 
          </p>
          <p>
          People are able to go there and collect free drinking water as long as you bring your own containers. And with the recent upgrade to their dispensers it can fill up large containers quite fast as well. Meaning that you only need to go there every once in a while (depending on how many containers you fill).
          </p>
          <p>
          The fountain gets water from a cave deep below the ground which it then filters for the public to drink.
          </p>
          <p>
          There are other, safer ways to drink water but you shouldn’t be afraid. The chances of something like this happening again are quite low. These other ways to get water may be more inconvenient but can be a safer way to drink good and healthy water in New Zealand.
          </p>
          
